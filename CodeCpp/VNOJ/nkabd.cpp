#include <bits/stdc++.h>
using namespace std;

const int N = 1e5+1;
int l, r;
long long sum[N];

void solve() {
    cin >> l >> r;
    for (int i = 1; i <= r; i++) for (int j = i; j <= r; j += i)
        sum[j] += i;
    int ans = 0;
    for (int i = l; i <= r; i++) ans += (sum[i]-i > i);
    cout << ans;
}

int main() {
    ios_base::sync_with_stdio(0); cin.tie(0); cout.tie(0);
    #define TASK "TASK"
    if (fopen(TASK".inp", "r")) {
        freopen(TASK".inp", "r", stdin);
        freopen(TASK".out", "w", stdout);
    }
    int t = 1;
    // cin >> t;
    while (t--) {
        solve();
    }
}
